//////////////////////////////////////////////////////////////////////////////
//ヘッダーファイルインクルード
//////////////////////////////////////////////////////////////////////////////
#include <d3dx9.h>
#include "common.h"

//////////////////////////////////////////////////////////////////////////////
//グローバル変数宣言
//////////////////////////////////////////////////////////////////////////////
static LPD3DXFONT g_pD3DXFont = NULL;

//////////////////////////////////////////////////////////////////////////////
//関数名	: FontLoad
//戻り値	:
//説明		:
//////////////////////////////////////////////////////////////////////////////
void FontLoad(void)
{
	LPDIRECT3DDEVICE9 pDevice = Direct3D_GetDevice();
	
	if (!pDevice)
	{
		return;
	}

	D3DXCreateFont(pDevice, 18, 0, 0, 0,
					FALSE, SHIFTJIS_CHARSET, OUT_DEFAULT_PRECIS, DEFAULT_QUALITY,
					DEFAULT_PITCH, "Terminal", &g_pD3DXFont
					);
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: FontRelease
//戻り値	:
//説明		:
//////////////////////////////////////////////////////////////////////////////
void FontRelease(void)
{
	if (g_pD3DXFont)
	{
		g_pD3DXFont->Release();
		g_pD3DXFont = NULL;
	}
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: Font_GetDevice
//戻り値	:
//説明		:
//////////////////////////////////////////////////////////////////////////////
void PrintFont(void)
{
	RECT rect = { 0, 0, SCREEN_WIDTH, SCREEN_HEIGHT };

	g_pD3DXFont->DrawText(NULL, "文\n字列", -1, &rect, DT_LEFT, D3DCOLOR_RGBA(0, 255, 0, 255));
}