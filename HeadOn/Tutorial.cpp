#include "common.h"
#include "Tutorial.h"

static bool g_bNextScene = false;

//////////////////////////////////////////////////////////////////////////////
//関数名	:
//戻り値	:
//説明		:
//引数		:
//////////////////////////////////////////////////////////////////////////////
void TutorialInit(void)
{
	g_bNextScene = false;

	FadeStart(false, 90, D3DCOLOR_RGBA(0, 0, 0, 255));
}

//////////////////////////////////////////////////////////////////////////////
//関数名	: TutorialUpdate
//戻り値	:
//説明		:
//引数		:
//////////////////////////////////////////////////////////////////////////////
void TutorialUpdate(void)
{
	//SceneChange(SCENE_INDEX_GAME);	//デバッグ用

	if (!g_bNextScene)
	{
		if (key.getTrigger(DIK_SPACE))
		{
			FadeStart(true, 10, D3DCOLOR_RGBA(0, 0, 0, 255));

			g_bNextScene = true;
			
			//SceneChange(SCENE_INDEX_GAME);
		}
	}

	else
	{
		if (!FadeIsFade())
		{
			SceneChange(SCENE_INDEX_GAME);
		}
	}

	
	/*if (Keyboard_IsPress(DIK_SPACE))
	{
		SceneChange(SCENE_INDEX_GAME);
	}*/	
}

